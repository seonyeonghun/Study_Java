public class Characters {
    public static void main(String[] args) {
        // 문자
        // a, b , 가, 나 - 1개의 문자
        char charA = 'A'; // 작은 따옴표
        System.out.println(charA);

        // ab가나 문자열
        // String : 문자열 첫글자는 참조형(Reference Type) vs 원시형(Primitives Type)
        String hello = "안녕하세요"; // 큰따옴표
        System.out.println(hello);
    }
}
